def init_seedance_v2_seed(db):
    """初始化 Seedance V2 种子数据（幂等）"""
    # 检查是否已初始化
    existing = db.execute("SELECT COUNT(*) FROM prompt_library").fetchone()[0]
    if existing > 0:
        print("[Seedance V2] 词库已初始化，跳过")
        return

    print("[Seedance V2] 初始化 27 套维度词库...")

    # 1. 插入 27 个词库
    lib_id_map = {}
    for idx, lib in enumerate(LIBRARIES):
        cur = db.execute(
            "INSERT INTO prompt_library (dimension_key, dimension_name, category, sort_order, description) VALUES (?, ?, ?, ?, ?)",
            (lib["key"], lib["name"], lib["cat"], idx, lib["desc"])
        )
        lib_id_map[lib["key"]] = cur.lastrowid

    # 2. 插入初始词卡
    total_cards = 0
    for lib_key, lib_id in lib_id_map.items():
        cards = get_initial_cards(lib_key)
        for card in cards:
            word_text, definition, weight = card
            db.execute(
                "INSERT INTO prompt_word_card (library_id, word_text, definition, heat_weight, is_system, usage_count) VALUES (?, ?, ?, ?, 1, 0)",
                (lib_id, word_text, definition, weight)
            )
            total_cards += 1

    # 3. 插入默认全局配置
    db.execute(
        "INSERT OR IGNORE INTO sys_global_config (config_key, config_value, description) VALUES (?, ?, ?)",
        ("seedance_default_duration", "15", "Seedance默认总时长(秒)")
    )
    db.execute(
        "INSERT OR IGNORE INTO sys_global_config (config_key, config_value, description) VALUES (?, ?, ?)",
        ("seedance_default_aspect_ratio", "16:9", "默认画幅比例")
    )
    db.execute(
        "INSERT OR IGNORE INTO sys_global_config (config_key, config_value, description) VALUES (?, ?, ?)",
        ("seedance_default_resolution", "4K", "默认分辨率")
    )
    db.execute(
        "INSERT OR IGNORE INTO sys_global_config (config_key, config_value, description) VALUES (?, ?, ?)",
        ("seedance_max_duration", "15", "最大时长限制(秒)")
    )

    print(f"[Seedance V2] 初始化完成: {len(LIBRARIES)} 套词库 / {total_cards} 张词卡")
